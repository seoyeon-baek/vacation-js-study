//변수란?
// 작성한 프로그램이 실행되는 도중에 임의의 값을 저장해두고 읽을 수 있는 가상의 공간

// 변수 선언 (변수는 여러개 선언 가능하다)
var a;
let b,c;
const d,e,f;	//상수 (값이 변하지 않음)

// 변수 초기화란?
// 변수를 선언하고 처음으로 저장하는 값
a = 10;
b,c = 20;
d = 30;
e,f = 40;

//요약
var a = 10;
let b,c = 20;
const d = 30;
const e,f = 40;

// 변수에 값 저장하기 (초기화 한 값을 변경)
a = 30;
b = 25;
c = 40;
d,e = 50;
f = "값 변경 완료";

